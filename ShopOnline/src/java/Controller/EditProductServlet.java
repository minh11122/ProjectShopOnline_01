    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            int pid = Integer.parseInt(request.getParameter("pid"));
            ProductDAO dao = new ProductDAO();
            ProductDTO dataProduct = dao.getDataProductByID(pid);
            HttpSession session = request.getSession();
            session.setAttribute("dataProduct", dataProduct);
            request.setAttribute("pid", pid);

            request.getRequestDispatcher("edit_Product.jsp").forward(request, response);
        } catch (SQLException ex) {
            Logger.getLogger(EditProductServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            int pid = Integer.parseInt(request.getParameter("txtId"));

            String txtName = request.getParameter("txtName");
            String txtImg = request.getParameter("txtImg");
            double txtPrice = Double.parseDouble(request.getParameter("txtPrice"));
            String txtTitle = request.getParameter("txtTitle");
            String txtDescription = request.getParameter("txtDescription");

            ProductDAO dao = new ProductDAO();

            boolean result = dao.updateProduct(pid, txtName, txtImg, txtPrice, txtTitle, txtDescription);
            request.setAttribute("mess", "Update successful!! ");

            request.getRequestDispatcher("edit_Product.jsp").forward(request, response);
        } catch (SQLException ex) {
            Logger.getLogger(EditProductServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

